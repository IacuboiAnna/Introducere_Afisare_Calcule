#De la tastatură se întroduce numărul de rînd al culorii curcubeului. De afişat
#denumirea culorii. Convenim că culoarea roşie are numărul de rînd 1.
a=int(input("Dati numarul de rand al culorii:"))
if a==1:
    print("rosu")
elif a==2:
    print("oranj")
elif a==3:
    print("galben")
elif a==4:
    print("verde")
elif a==5:
    print("albastru")
elif a==6:
    print("indigo")
elif a==7:
    print("violet")
else:
    print("Invata culorile!")